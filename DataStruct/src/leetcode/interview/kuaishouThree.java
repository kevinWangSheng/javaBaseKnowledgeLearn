    public int uniquePathsWithObstacles(int[][] obstacleGrid) {
        n = obstacleGrid.length;
        m = obstacleGrid[0].length;
        mark = new int[n+1][m+1];
        dfs(obstacleGrid,0,0);
        return mark[0][0];

    }


    public int dfs(int [][] obstacleGrid,int row,int col){
        if(isOutOfFlow(row,col) || obstacleGrid[row][col]==1){
            return 0 ;
        }
        if(row==n-1 && col==m-1 ){
            mark[row][col] = 1;
            return 1;
        }
        // 已经遍历过了
        if(obstacleGrid[row][col]==2){
            mark[row][col] += mark[row+1][col]+mark[row][col+1];
        }
        else{
            obstacleGrid[row][col] = 2;
            dfs(obstacleGrid,row+1,col);
            dfs(obstacleGrid,row,col+1);
            mark[row][col] = mark[row+1][col]+mark[row][col+1];
        }

        return mark[row][col];
    }

    public boolean isOutOfFlow(int row,int col){
        if(row<0 || col<0 || row>=n || col>=m){
            return true;
        }
        return false;

    }
